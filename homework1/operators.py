#Following the examples found at https://python.swaroopch.com/op_exp.html


#plus

v = 3+5
print(v)

#minus
v -= 5
print(v)

#multiply
v *= 5
print(v)

#power
v **= 2
print(v)

#divide
v /= 2
print(v)

#divide and floor
v //= 2
print(v)

#modulo
v %= 3
print(v)

#left shift
x = 2
x <<= 2
print(x)

#right shift
x >>= 2
print(x)

#bit-wise AND
x = 5 & 3
print(x)

#bit-wise OR
x = 5 | 3
print(x)

#bit-wise XOR
x = 5 ^ 3
print(x)

#bit-wise invert
x = ~5
print(x)

#comparisons
x = 5
print(x < 10)
print(x < 4)
print(x < 5)
print(x <= 5)
print(x >= 5)
print(x == 5)
print(x != 5)

x = True
z = not(x)
y = x and z
u = x or z
print(z)
print(y)
print(u)


